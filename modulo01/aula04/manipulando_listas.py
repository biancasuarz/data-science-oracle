raca_caes = ['Labrador Retriever',
             'Bulldog Francês',
             'Pastor Alemão',
             'Poodle']

#insert para adicionar indice, elemento
raca_caes.insert(1, 'Golden Retriever')
print(raca_caes)

#pop remove determinado elemento da lista
raca_caes.pop(1)
print(raca_caes)

#sort organiza elementos em ordem crescente ou decrescente
raca_caes.sort()
print(raca_caes)